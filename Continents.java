public class Continents {
  // Learn to use switch
	public static void main(String[] args) {
    int continent = 2;
    switch(continent){
      case(1):
      System.out.println("North America: Mexico City, Mexico.");
      break;
      case(2):
      System.out.println("Australia: Sydney, Australia");
      break;
      default:
      System.out.println("Undefined continent!");
      break;
    }

		

	}
}
